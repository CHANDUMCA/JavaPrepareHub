package com.sunrizeit.week1;

public class Program5_Loops {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello Java " + i);
        }
        int j = 1;
        while (j <= 5) {
            System.out.println("Count: " + j);
            j++;

        }

    }
}



